    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        Productos producto = new Productos();
        producto.setCodigo(Integer.parseInt(txtBuscar.getText()));
        DefaultTableModel modelo = (DefaultTableModel) tblTienda.getModel();        
        boolean Existe = false;
        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (modelo.getValueAt(i, 0).equals(String.valueOf(producto.getCodigo()))) {
                producto.setCodigo(Integer.parseInt(modelo.getValueAt(i, 0).toString()));
                producto.setNombre(modelo.getValueAt(i, 1).toString());
                producto.setCategoria(modelo.getValueAt(i, 2).toString());
                producto.setPrecio(Integer.parseInt(modelo.getValueAt(i, 3).toString()));
                JOptionPane.showMessageDialog(null, "Datos del producto ingresado: \n" +
                        "Codigo: "+ producto.getCodigo() + "\n" +
                        "Nombre: "+ producto.getNombre() + "\n" +
                        "Categoria: "+ producto.getCategoria() + "\n" +
                        "Precio: "+ producto.getPrecio());
                Existe = true;
                break;
            }
        }
        if (!Existe) {
            JOptionPane.showMessageDialog(null, "No se encontro el codigo");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

