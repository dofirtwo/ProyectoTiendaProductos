    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Productos producto = new Productos();
        try {
            if (!txtCodigo.getText().isEmpty() && !txtNombre.getText().isEmpty() && !txtCategoria.getText().isEmpty() && !txtPrecio.getText().isEmpty()) {
                producto.setCodigo(Integer.parseInt(txtCodigo.getText()));
                producto.setNombre(txtNombre.getText());
                producto.setCategoria(txtCategoria.getText());
                producto.setPrecio(Integer.parseInt(txtPrecio.getText()));
                DefaultTableModel modelo = (DefaultTableModel) tblTienda.getModel();
                String[] datos = new String[4];
                datos[0] = String.valueOf(producto.getCodigo());
                datos[1] = producto.getNombre();
                datos[2] = producto.getCategoria();
                datos[3] = String.valueOf(producto.getPrecio());
                boolean Existe = false;
                for (int i = 0; i < modelo.getRowCount(); i++) {
                    Object s = modelo.getValueAt(i, 0);
                    if (datos[0].equals(s)) {
                        Existe = true;
                        break;
                    }
                }
                if (!Existe) {
                    modelo.addRow(datos);
                } else {
                    JOptionPane.showMessageDialog(null, "Codigo ya ingresado");
                }
            } else {
                JOptionPane.showMessageDialog(null, "falta ingresar un dato");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "el formato debe de ser un numero");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

